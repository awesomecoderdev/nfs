        <h3>Finanzierung und Einsatzgebiet</h3>
        </p>


        <p>Die Trägerschaft für die Notfallseelsorge Darmstadt-Dieburg hat das evangelische Dekanat Darmstadt-Stadt. Das
            Einsatzgebiet umfasst die Stadt Darmstadt und den westlichen Teil des Landkreises Darmstadt-Dieburg. Die
            Notfallseelsorge Darmstadt und Umgebung arbeitet mit zwei Notarztwachen und sieben Rettungswachen zusammen.
        </p>

        <p>Finanziert wird die Notfallseelsorge von der Evangelischen Kirche in Hessen und Nassau (EKHN), den beiden
            Dekanaten Darmstadt-Stadt und Darmstadt-Land, dem Bistum Mainz, aus Kirchensteuermitteln und Spenden. </p>
        <br />

        <p>
        <h3>Leitung und weitere Aufgaben</h3>
        </p>

        <p>Pfarrer Heiko Ruff-Kapraun leitet die Notfallseelsorge Darmstadt und Umgebung mit einer halben
            Sonderpfarrstelle. Mit einem Stellenanteil von sechs Stunden wöchentlich ist die Gemeindereferentin des
            Bistums Mainz, Susanne Fitz, in die Arbeit eingebunden.

        <p>In der Notfallseelsorge und Einsatznachsorge arbeiten rund 55 Personen ehrenamtlich mit. Die Aufgaben werden
            in einem Leitungsteam abgestimmt
            und im Konsens mit den Mitarbeitenden gestaltet.</p>

        <p>Im Jahr 2008 erweiterte die Notfallseelsorge ihr Aufgabengebiet um die Einsatznachsorge für die
            Mitarbeitenden bei Feuerwehr und Rettungsdienst. Seit 2015 kooperiert die Notfallseelsorge mit den
            Betreuungszügen des Deutschen Roten Kreuzes Darmstadt. Die Aufgaben "Betreuung von Betroffenen" werden in
            einem eigenen Aufgabenfeld in der Rettungskette geführt werden. </p>



