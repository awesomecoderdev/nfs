        <h3>Organisation der Notfallseelsorge Darmstadt und Umgebung</h3>

        <p>1999 kann als Geburtsjahr der Notfallseelsorge Darmstadt-Dieburg gelten. In diesem Jahr hat sich ein Team aus
            Krankenhausseelsorgern zusammengefunden und mit der Planung einer Notfallseelsorge begonnen. </p>

        <p>Die beiden Dekanate Darmstadt-Stadt und Darmstadt-Land beteiligten sich am Aufbau dieser Organisation für
